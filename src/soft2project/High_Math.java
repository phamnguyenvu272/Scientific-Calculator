package soft2project;

public class High_Math {

	private High_Math() {}
	
public static double dSqrt(double x){
	double z = Math.sqrt(x);
	return z;}
	
public static double dPow(double x, double y){
	double z = Math.pow(x,y);
	return z;}
public static double dLoge(double x){
	double z= Math.log(x);
	return z;}
public static double dLog10(double x){
	double z = Math.log10(x);
	return z;}

public static double dSqr(double x){
	double z = Math.pow(x,2);
	return z;}

public static double dNrt(double x, double y){
	double z = Math.pow(x,(1/y));
	return z;}

public static double dSin(double x) {
	double z = Math.sin(x);
	return z;
}
public static double dCos(double x) {
	double z = Math.cos(x);
	return z;
}
	
public static double dTan(double x) {
	double z = Math.tan(x);
	return z;
}
	
}




